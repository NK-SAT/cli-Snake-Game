public class Snake {
    private int indexi;
    private int indexj;

    public Snake(int indexi, int indexj) {
        this.indexi = indexi;
        this.indexj = indexj;
    }

    public int getIndexi() {
        return indexi;
    }

    public void setIndexi(int indexi) {
        this.indexi = indexi;
    }

    public int getIndexj() {
        return indexj;
    }

    public void setIndexj(int indexj) {
        this.indexj = indexj;
    }
}
